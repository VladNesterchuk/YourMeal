






// Open & Close DessertIceCream
{
    const productDetailButtonDessertIceCream = document.getElementById('DessertIceCream');
    const modalDessertIceCream = document.getElementById('DessertIceCreamModal');
    const modalCloseButtonDessertIceCream = document.getElementById('ModalCloseDessertIceCream');
    
    productDetailButtonDessertIceCream.addEventListener('click', function() {
        modalDessertIceCream.classList.add('modal_open');
    });
    
    modalCloseButtonDessertIceCream.addEventListener('click', function() {
        modalDessertIceCream.classList.remove('modal_open');
    });
    }

