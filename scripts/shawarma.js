// Open & Close ShawarmaChicken
{
    const productDetailButtonShawarmaChicken = document.getElementById('ShawarmaChicken');
    const modalShawarmaChicken = document.getElementById('ShawarmaChickenModal');
    const modalCloseButtonShawarmaChicken = document.getElementById('ModalCloseShawarmaChicken');
    
    productDetailButtonShawarmaChicken.addEventListener('click', function() {
        modalShawarmaChicken.classList.add('modal_open');
    });
    
    modalCloseButtonShawarmaChicken.addEventListener('click', function() {
        modalShawarmaChicken.classList.remove('modal_open');
    });
    }
    
