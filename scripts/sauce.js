// Open & Close SauceBBQ 
{
    const productDetailButtonSauceBBQ = document.getElementById('SauceBBQ');
    const modalSauceBBQ = document.getElementById('SauceBBQModal');
    const modalCloseButtonSauceBBQ = document.getElementById('ModalCloseSauceBBQ');
    
    productDetailButtonSauceBBQ.addEventListener('click', function() {
        modalSauceBBQ.classList.add('modal_open');
    });
    
    modalCloseButtonSauceBBQ.addEventListener('click', function() {
        modalSauceBBQ.classList.remove('modal_open');
    });
    }
  
