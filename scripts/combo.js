// Open & Close SFC
{
    const productDetailButtonSFC = document.getElementById('SFC');
    const modalSFC = document.getElementById('SFCModal');
    const modalCloseButtonSFC = document.getElementById('ModalCloseSFC');
    
    productDetailButtonSFC.addEventListener('click', function() {
        modalSFC.classList.add('modal_open');
    });
    
    modalCloseButtonSFC.addEventListener('click', function() {
        modalSFC.classList.remove('modal_open');
    });
    }
    
