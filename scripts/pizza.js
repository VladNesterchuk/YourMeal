// Open & Close PizzaSea
{
    const productDetailButtonPizzaSea = document.getElementById('PizzaSea');
    const modalPizzaSea = document.getElementById('PizzaSeaModal');
    const modalCloseButtonPizzaSea = document.getElementById('ModalClosePizzaSea');
    
    productDetailButtonPizzaSea.addEventListener('click', function() {
        modalPizzaSea.classList.add('modal_open');
    });
    
    modalCloseButtonPizzaSea.addEventListener('click', function() {
        modalPizzaSea.classList.remove('modal_open');
    });
    }
  
