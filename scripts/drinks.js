// Open & Close DrinksCola
{
    const productDetailButtonDrinksCola = document.getElementById('DrinksCola');
    const modalDrinksCola = document.getElementById('DrinksColaModal');
    const modalCloseButtonDrinksCola = document.getElementById('ModalCloseDrinksCola');
    
    productDetailButtonDrinksCola.addEventListener('click', function() {
        modalDrinksCola.classList.add('modal_open');
    });
    
    modalCloseButtonDrinksCola.addEventListener('click', function() {
        modalDrinksCola.classList.remove('modal_open');
    });
    }

