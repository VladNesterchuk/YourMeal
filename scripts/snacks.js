// Open & Close OnionRings
 {
    const productDetailButtonOnionRings = document.getElementById('OnionRings');
    const modalOnionRings = document.getElementById('OnionRingsModal');
    const modalCloseButtonOnionRings = document.getElementById('ModalCloseOnionRings');
    
    productDetailButtonOnionRings.addEventListener('click', function() {
        modalOnionRings.classList.add('modal_open');
    });
    
    modalCloseButtonOnionRings.addEventListener('click', function() {
        modalOnionRings.classList.remove('modal_open');
    });
    }

